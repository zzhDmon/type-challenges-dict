type Absolute<T extends number | string | bigint> =
  `${T}` extends `-${infer R}` ? `${R}` : `${T}`

/*
## 注释
  1. **使用模板字面量类型**
    ```typescript
    `${T}`
    ```
    - 将 `T` 转换为字符串类型，无论 `T` 是 `number`、`string` 还是 `bigint`。
    - 模板字面量类型会将 `T` 自动转换为字符串表示。
  2. **条件类型判断是否为负数**
    ```typescript
    `${T}` extends `-${infer R}` ? `${R}` : `${T}`
    ```
    - 使用条件类型判断字符串类型的 `T` 是否匹配 `'-'` 前缀模式。
    - 如果 `T` 是以 `'-'` 开头的字符串（例如 `'-5'`），则提取负号后的部分 `R`，并返回 `R` 的字符串表示。
    - 如果 `T` 不是负数（例如 `'5'` 或 `'10'`），直接返回 `T` 的字符串表示。
  这种方法确保了对于所有输入类型 `number`、`string` 和 `bigint` 都能正确处理，并返回其绝对值的字符串表示。
*/
